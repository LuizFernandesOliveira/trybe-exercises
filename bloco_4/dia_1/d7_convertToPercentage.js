const percentage = 80;
const text = 'Conceito: '

if(percentage >= 90) console.log(text+'A');
else if(percentage >= 80) console.log(text+'B');
else if(percentage >= 70) console.log(text+'C');
else if(percentage >= 60) console.log(text+'D');
else if(percentage >= 50) console.log(text+'E');
else if(percentage < 50) console.log(text+'F');